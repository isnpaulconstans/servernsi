        <h2>
            Jupyter
        </h2>
        <p>
            Jupyter est une application web utilisée pour programmer dans
            plus de 40 langages de programmation, dont Python, Julia,
            Ruby, R, ou encore Scala2. Jupyter est une évolution du projet
            IPython. Jupyter permet de réaliser des calepins ou notebooks,
            c'est-à-dire des programmes contenant à la fois du texte en
            markdown et du code en Julia, Python, R...
            Ces calepins sont utilisés en science des données pour
            explorer et analyser des données.
        </p>
